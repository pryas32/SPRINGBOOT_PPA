package Exception.Reflection;

public class Sciencemarks {
    private final int physics;
    private final int mathsmarks;
    private final double scipercent;

public Sciencemarks(int physics,int mathsmarks,double scipercent)
{
    this.physics=physics;
    this.mathsmarks=mathsmarks;
    this.scipercent=scipercent;
}

public int getphysics()
{
    return physics;
}



public int getmathsmarks()
{
    return mathsmarks;
}
 
public double getsciencepercent()
{
    return scipercent;
}

public int getPhysics() {
    return physics;
}

public int getMathsmarks() {
    return mathsmarks;
}

public double getScipercent() {
    return scipercent;
}
}
